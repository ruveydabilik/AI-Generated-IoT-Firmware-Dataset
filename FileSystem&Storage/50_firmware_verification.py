import hashlib
import hmac
import json
import os

# In a real hobby project, store this in a secure environment variable
# or a protected config file, not hardcoded!
SHARED_SECRET = b"my_ultra_secure_hobbyist_key_123"

def process_input(data):
    """
    Verifies a firmware file using SHA-256 and HMAC.

    Expected JSON input:
    {
        "file_path": "firmware_v2.bin",
        "expected_hmac": "hex_string_here"
    }
    """
    try:
        # 1. Parse the incoming JSON
        request = json.loads(data)
        file_path = request.get("file_path")
        provided_hmac = request.get("expected_hmac")

        if not file_path or not provided_hmac:
            return json.dumps({"status": "error", "message": "Missing file_path or expected_hmac"})

        if not os.path.exists(file_path):
            return json.dumps({"status": "error", "message": "Firmware file not found"})

        # 2. Calculate the SHA-256 hash of the file in chunks
        # (Crucial for IoT devices with limited RAM!)
        sha256_hash = hashlib.sha256()

        with open(file_path, "rb") as f:
            for byte_block in iter(lambda: f.read(4096), b""):
                sha256_hash.update(byte_block)

        calculated_hash = sha256_hash.digest()

        # 3. Generate HMAC to verify authenticity
        # This proves the hash was generated by someone with the SHARED_SECRET
        mac = hmac.new(SHARED_SECRET, calculated_hash, hashlib.sha256)
        actual_hmac = mac.hexdigest()

        # 4. Securely compare the provided HMAC with our calculated one
        # Use compare_digest to prevent timing attacks
        if hmac.compare_digest(actual_hmac, provided_hmac):
            return json.dumps({
                "status": "verified",
                "message": "Firmware integrity and authenticity confirmed.",
                "hash": sha256_hash.hexdigest()
            })
        else:
            return json.dumps({
                "status": "failed",
                "message": "Verification failed! The file may be corrupted or malicious."
            })

    except json.JSONDecodeError:
        return json.dumps({"status": "error", "message": "Invalid JSON input"})
    except Exception as e:
        return json.dumps({"status": "error", "message": str(e)})

